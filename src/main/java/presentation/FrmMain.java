    public void actualiserListeAmis(){
        LstAmis.removeAll();
        lblBienvenue.setText("Bienvenue " + owner.getLogin());
        lstF = new ListeUserFriends(owner.getId());
        for (int i = 0; i < lstF.size(); i++) {
            LstAmis.add(lstF.get(i).getLogin());
        }
    }

    
